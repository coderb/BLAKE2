			v3 += v7 + m[6];  v15 ^= v3; v15 = (v15 >>  8) | (v15 << 24);
			v11 += v15;        v7 ^= v11; v7  = (v7  >>  7) | (v7  << 25);
			// G(1,4, v0,v5,v10,v15)  m[1], m[12]
			v0 += v5 + m[1];  v15 ^= v0; v15 = (v15 >> 16) | (v15 << 16);
			v10 += v15;        v5 ^= v10; v5  = (v5  >> 12) | (v5  << 20);
			v0 += v5 + m[12]; v15 ^= v0; v15 = (v15 >>  8) | (v15 << 24);
			v10 += v15;        v5 ^= v10; v5  = (v5  >>  7) | (v5  << 25);
			// G(1,5, v1,v6,v11,v12)  m[0], m[2]
			v1 += v6 + m[0];  v12 ^= v1; v12 = (v12 >> 16) | (v12 << 16);

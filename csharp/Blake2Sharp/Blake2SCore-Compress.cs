// BLAKE2s compression function — fully unrolled, 10 rounds.
//
// G mixing function rotation constants (differ from BLAKE2b):
//   R1=16, R2=12, R3=8, R4=7
//
// G(a,b,c,d, mx, my):
//   a += b + mx;  d = rotr32(d^a, 16);
//   c += d;       b = rotr32(b^c, 12);
//   a += b + my;  d = rotr32(d^a,  8);
//   c += d;       b = rotr32(b^c,  7);
//
// Each round applies 8 G calls (4 column + 4 diagonal):
//   col: G(v0,v4,v8, v12)  G(v1,v5,v9, v13)  G(v2,v6,v10,v14)  G(v3,v7,v11,v15)
//   dia: G(v0,v5,v10,v15)  G(v1,v6,v11,v12)  G(v2,v7,v8, v13)  G(v3,v4,v9, v14)

using System;

namespace Blake2Sharp
{
	public sealed partial class Blake2SCore
	{
		partial void Compress(byte[] block, int start)
		{
			var m = _m;
			var h = _h;

			if (BitConverter.IsLittleEndian)
			{
				Buffer.BlockCopy(block, start, m, 0, BlockSizeInBytes);
			}
			else
			{
				for (int i = 0; i < 16; i++)
					m[i] = BytesToUInt32(block, start + i * 4);
			}

			uint v0  = h[0];
			uint v1  = h[1];
			uint v2  = h[2];
			uint v3  = h[3];
			uint v4  = h[4];
			uint v5  = h[5];
			uint v6  = h[6];
			uint v7  = h[7];
			uint v8  = IV0;
			uint v9  = IV1;
			uint v10 = IV2;
			uint v11 = IV3;
			uint v12 = IV4 ^ _counter0;
			uint v13 = IV5 ^ _counter1;
			uint v14 = IV6 ^ (_finalizationFlag0 ? uint.MaxValue : 0u);
			uint v15 = IV7 ^ (_finalizationFlag1 ? uint.MaxValue : 0u);

			// ##### Round 0 #####
			// G(0,0, v0,v4,v8, v12)  m[0], m[1]
			v0 += v4 + m[0];  v12 ^= v0; v12 = (v12 >> 16) | (v12 << 16);
			v8 += v12;         v4 ^= v8;  v4  = (v4  >> 12) | (v4  << 20);
			v0 += v4 + m[1];  v12 ^= v0; v12 = (v12 >>  8) | (v12 << 24);
			v8 += v12;         v4 ^= v8;  v4  = (v4  >>  7) | (v4  << 25);
			// G(0,1, v1,v5,v9, v13)  m[2], m[3]
			v1 += v5 + m[2];  v13 ^= v1; v13 = (v13 >> 16) | (v13 << 16);
			v9 += v13;         v5 ^= v9;  v5  = (v5  >> 12) | (v5  << 20);
			v1 += v5 + m[3];  v13 ^= v1; v13 = (v13 >>  8) | (v13 << 24);
			v9 += v13;         v5 ^= v9;  v5  = (v5  >>  7) | (v5  << 25);
			// G(0,2, v2,v6,v10,v14)  m[4], m[5]
			v2 += v6 + m[4];  v14 ^= v2; v14 = (v14 >> 16) | (v14 << 16);
			v10 += v14;        v6 ^= v10; v6  = (v6  >> 12) | (v6  << 20);
			v2 += v6 + m[5];  v14 ^= v2; v14 = (v14 >>  8) | (v14 << 24);
			v10 += v14;        v6 ^= v10; v6  = (v6  >>  7) | (v6  << 25);
			// G(0,3, v3,v7,v11,v15)  m[6], m[7]
			v3 += v7 + m[6];  v15 ^= v3; v15 = (v15 >> 16) | (v15 << 16);
			v11 += v15;        v7 ^= v11; v7  = (v7  >> 12) | (v7  << 20);
			v3 += v7 + m[7];  v15 ^= v3; v15 = (v15 >>  8) | (v15 << 24);
			v11 += v15;        v7 ^= v11; v7  = (v7  >>  7) | (v7  << 25);
			// G(0,4, v0,v5,v10,v15)  m[8], m[9]
			v0 += v5 + m[8];  v15 ^= v0; v15 = (v15 >> 16) | (v15 << 16);
			v10 += v15;        v5 ^= v10; v5  = (v5  >> 12) | (v5  << 20);
			v0 += v5 + m[9];  v15 ^= v0; v15 = (v15 >>  8) | (v15 << 24);
			v10 += v15;        v5 ^= v10; v5  = (v5  >>  7) | (v5  << 25);
			// G(0,5, v1,v6,v11,v12)  m[10], m[11]
			v1 += v6 + m[10]; v12 ^= v1; v12 = (v12 >> 16) | (v12 << 16);
			v11 += v12;        v6 ^= v11; v6  = (v6  >> 12) | (v6  << 20);
			v1 += v6 + m[11]; v12 ^= v1; v12 = (v12 >>  8) | (v12 << 24);
			v11 += v12;        v6 ^= v11; v6  = (v6  >>  7) | (v6  << 25);
			// G(0,6, v2,v7,v8, v13)  m[12], m[13]
			v2 += v7 + m[12]; v13 ^= v2; v13 = (v13 >> 16) | (v13 << 16);
			v8 += v13;         v7 ^= v8;  v7  = (v7  >> 12) | (v7  << 20);
			v2 += v7 + m[13]; v13 ^= v2; v13 = (v13 >>  8) | (v13 << 24);
			v8 += v13;         v7 ^= v8;  v7  = (v7  >>  7) | (v7  << 25);
			// G(0,7, v3,v4,v9, v14)  m[14], m[15]
			v3 += v4 + m[14]; v14 ^= v3; v14 = (v14 >> 16) | (v14 << 16);
			v9 += v14;         v4 ^= v9;  v4  = (v4  >> 12) | (v4  << 20);
			v3 += v4 + m[15]; v14 ^= v3; v14 = (v14 >>  8) | (v14 << 24);
			v9 += v14;         v4 ^= v9;  v4  = (v4  >>  7) | (v4  << 25);

			// ##### Round 1 #####
			// G(1,0, v0,v4,v8, v12)  m[14], m[10]
			v0 += v4 + m[14]; v12 ^= v0; v12 = (v12 >> 16) | (v12 << 16);
			v8 += v12;         v4 ^= v8;  v4  = (v4  >> 12) | (v4  << 20);
			v0 += v4 + m[10]; v12 ^= v0; v12 = (v12 >>  8) | (v12 << 24);
			v8 += v12;         v4 ^= v8;  v4  = (v4  >>  7) | (v4  << 25);
			// G(1,1, v1,v5,v9, v13)  m[4], m[8]
			v1 += v5 + m[4];  v13 ^= v1; v13 = (v13 >> 16) | (v13 << 16);
			v9 += v13;         v5 ^= v9;  v5  = (v5  >> 12) | (v5  << 20);
			v1 += v5 + m[8];  v13 ^= v1; v13 = (v13 >>  8) | (v13 << 24);
			v9 += v13;         v5 ^= v9;  v5  = (v5  >>  7) | (v5  << 25);
			// G(1,2, v2,v6,v10,v14)  m[9], m[15]
			v2 += v6 + m[9];  v14 ^= v2; v14 = (v14 >> 16) | (v14 << 16);
			v10 += v14;        v6 ^= v10; v6  = (v6  >> 12) | (v6  << 20);
			v2 += v6 + m[15]; v14 ^= v2; v14 = (v14 >>  8) | (v14 << 24);
			v10 += v14;        v6 ^= v10; v6  = (v6  >>  7) | (v6  << 25);
			// G(1,3, v3,v7,v11,v15)  m[13], m[6]
			v3 += v7 + m[13]; v15 ^= v3; v15 = (v15 >> 16) | (v15 << 16);

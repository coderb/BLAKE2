			v11 += v12;        v6 ^= v11; v6  = (v6  >>  7) | (v6  << 25);
			// G(1,6, v2,v7,v8, v13)  m[11], m[7]
			v2 += v7 + m[11]; v13 ^= v2; v13 = (v13 >> 16) | (v13 << 16);
			v8 += v13;         v7 ^= v8;  v7  = (v7  >> 12) | (v7  << 20);
			v2 += v7 + m[7];  v13 ^= v2; v13 = (v13 >>  8) | (v13 << 24);
			v8 += v13;         v7 ^= v8;  v7  = (v7  >>  7) | (v7  << 25);
			// G(1,7, v3,v4,v9, v14)  m[5], m[3]
			v3 += v4 + m[5];  v14 ^= v3; v14 = (v14 >> 16) | (v14 << 16);
			v9 += v14;         v4 ^= v9;  v4  = (v4  >> 12) | (v4  << 20);
			v3 += v4 + m[3];  v14 ^= v3; v14 = (v14 >>  8) | (v14 << 24);
			v9 += v14;         v4 ^= v9;  v4  = (v4  >>  7) | (v4  << 25);

			// ##### Round 2 #####
			// G(2,0, v0,v4,v8, v12)  m[11], m[8]

			v8 += v12;         v4 ^= v8;  v4  = (v4  >> 12) | (v4  << 20);
			v0 += v4 + m[8];  v12 ^= v0; v12 = (v12 >>  8) | (v12 << 24);
			v8 += v12;         v4 ^= v8;  v4  = (v4  >>  7) | (v4  << 25);
			// G(2,1, v1,v5,v9, v13)  m[12], m[0]
			v1 += v5 + m[12]; v13 ^= v1; v13 = (v13 >> 16) | (v13 << 16);
			v9 += v13;         v5 ^= v9;  v5  = (v5  >> 12) | (v5  << 20);
			v1 += v5 + m[0];  v13 ^= v1; v13 = (v13 >>  8) | (v13 << 24);
			v9 += v13;         v5 ^= v9;  v5  = (v5  >>  7) | (v5  << 25);
			// G(2,2, v2,v6,v10,v14)  m[5], m[2]
			v2 += v6 + m[5];  v14 ^= v2; v14 = (v14 >> 16) | (v14 << 16);
			v10 += v14;        v6 ^= v10; v6  = (v6  >> 12) | (v6  << 20);
			v2 += v6 + m[2];  v14 ^= v2; v14 = (v14 >>  8) | (v14 << 24);
			v10 += v14;        v6 ^= v10; v6  = (v6  >>  7) | (v6  << 25);
			// G(2,3, v3,v7,v11,v15)  m[15], m[13]
			v3 += v7 + m[15]; v15 ^= v3; v15 = (v15 >> 16) | (v15 << 16);
			v11 += v15;        v7 ^= v11; v7  = (v7  >> 12) | (v7  << 20);
			v3 += v7 + m[13]; v15 ^= v3; v15 = (v15 >>  8) | (v15 << 24);
			v11 += v15;        v7 ^= v11; v7  = (v7  >>  7) | (v7  << 25);
			// G(2,4, v0,v5,v10,v15)  m[10], m[14]
			v0 += v5 + m[10]; v15 ^= v0; v15 = (v15 >> 16) | (v15 << 16);
			v10 += v15;        v5 ^= v10; v5  = (v5  >> 12) | (v5  << 20);
			v0 += v5 + m[14]; v15 ^= v0; v15 = (v15 >>  8) | (v15 << 24);
			v10 += v15;        v5 ^= v10; v5  = (v5  >>  7) | (v5  << 25);
			// G(2,5, v1,v6,v11,v12)  m[3], m[6]
			v1 += v6 + m[3];  v12 ^= v1; v12 = (v12 >> 16) | (v12 << 16);
			v11 += v12;        v6 ^= v11; v6  = (v6  >> 12) | (v6  << 20);
			v1 += v6 + m[6];  v12 ^= v1; v12 = (v12 >>  8) | (v12 << 24);
			v11 += v12;        v6 ^= v11; v6  = (v6  >>  7) | (v6  << 25);
			// G(2,6, v2,v7,v8, v13)  m[7], m[1]
			v2 += v7 + m[7];  v13 ^= v2; v13 = (v13 >> 16) | (v13 << 16);
			v8 += v13;         v7 ^= v8;  v7  = (v7  >> 12) | (v7  << 20);
			v2 += v7 + m[1];  v13 ^= v2; v13 = (v13 >>  8) | (v13 << 24);
			v8 += v13;         v7 ^= v8;  v7  = (v7  >>  7) | (v7  << 25);
			// G(2,7, v3,v4,v9, v14)  m[9], m[4]
			v3 += v4 + m[9];  v14 ^= v3; v14 = (v14 >> 16) | (v14 << 16);
			v9 += v14;         v4 ^= v9;  v4  = (v4  >> 12) | (v4  << 20);
			v3 += v4 + m[4];  v14 ^= v3; v14 = (v14 >>  8) | (v14 << 24);
			v9 += v14;         v4 ^= v9;  v4  = (v4  >>  7) | (v4  << 25);

			// ##### Round 3 #####
			// G(3,0, v0,v4,v8, v12)  m[7], m[9]
			v0 += v4 + m[7];  v12 ^= v0; v12 = (v12 >> 16) | (v12 << 16);
			v8 += v12;         v4 ^= v8;  v4  = (v4  >> 12) | (v4  << 20);
			v0 += v4 + m[9];  v12 ^= v0; v12 = (v12 >>  8) | (v12 << 24);
			v8 += v12;         v4 ^= v8;  v4  = (v4  >>  7) | (v4  << 25);
			// G(3,1, v1,v5,v9, v13)  m[3], m[1]
			v1 += v5 + m[3];  v13 ^= v1; v13 = (v13 >> 16) | (v13 << 16);
			v9 += v13;         v5 ^= v9;  v5  = (v5  >> 12) | (v5  << 20);
			v1 += v5 + m[1];  v13 ^= v1; v13 = (v13 >>  8) | (v13 << 24);
			v9 += v13;         v5 ^= v9;  v5  = (v5  >>  7) | (v5  << 25);
			// G(3,2, v2,v6,v10,v14)  m[13], m[12]
			v2 += v6 + m[13]; v14 ^= v2; v14 = (v14 >> 16) | (v14 << 16);
			v10 += v14;        v6 ^= v10; v6  = (v6  >> 12) | (v6  << 20);
			v2 += v6 + m[12]; v14 ^= v2; v14 = (v14 >>  8) | (v14 << 24);
			v10 += v14;        v6 ^= v10; v6  = (v6  >>  7) | (v6  << 25);
			// G(3,3, v3,v7,v11,v15)  m[11], m[14]
			v3 += v7 + m[11]; v15 ^= v3; v15 = (v15 >> 16) | (v15 << 16);
			v11 += v15;        v7 ^= v11; v7  = (v7  >> 12) | (v7  << 20);
			v3 += v7 + m[14]; v15 ^= v3; v15 = (v15 >>  8) | (v15 << 24);
			v11 += v15;        v7 ^= v11; v7  = (v7  >>  7) | (v7  << 25);
			// G(3,4, v0,v5,v10,v15)  m[2], m[6]
			v0 += v5 + m[2];  v15 ^= v0; v15 = (v15 >> 16) | (v15 << 16);
			v10 += v15;        v5 ^= v10; v5  = (v5  >> 12) | (v5  << 20);
			v0 += v5 + m[6];  v15 ^= v0; v15 = (v15 >>  8) | (v15 << 24);
			v10 += v15;        v5 ^= v10; v5  = (v5  >>  7) | (v5  << 25);
			// G(3,5, v1,v6,v11,v12)  m[5], m[10]
			v1 += v6 + m[5];  v12 ^= v1; v12 = (v12 >> 16) | (v12 << 16);
